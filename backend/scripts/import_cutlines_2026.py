def run() -> None:
    db = SessionLocal()
    try:
        # 省份名 → id 映射
        province_map: dict[str, int] = {
            p.name: p.id for p in db.scalars(select(Province)).all()
        }

        batch_id = f"cutlines_2026_{date.today().strftime('%Y%m%d')}"
        now = datetime.utcnow()

        rows: list[dict] = []
        missing_provinces: list[str] = []

        for prov_name, entries in RAW_DATA.items():
            prov_id = province_map.get(prov_name)
            if prov_id is None:
                missing_provinces.append(prov_name)
                continue
            for track, batch, score in entries:
                rows.append({
                    "province_id":  prov_id,
                    "year":         YEAR,
                    "track":        track,
                    "batch":        batch,
                    "score":        score,
                    "import_batch": batch_id,
                    "created_at":   now,
                    "updated_at":   now,
                })

        if not rows:
            print("无数据可导入。")
            return

        stmt = pg_insert(ProvinceCutline).values(rows)
        stmt = stmt.on_conflict_do_update(
            constraint="uq_province_cutline",
            set_={
                "score":        stmt.excluded.score,
                "import_batch": stmt.excluded.import_batch,
                "updated_at":   stmt.excluded.updated_at,
            },
        )
        db.execute(stmt)
        db.commit()

        print(f"✓ 导入完成：{len(rows)} 条批次控制线 (year={YEAR})")
        print(f"  import_batch: {batch_id}")

        # 统计
        province_count = len({r["province_id"] for r in rows})
        print(f"  覆盖省份: {province_count} 个")

        if missing_provinces:
            print(f"  ⚠ 未匹配省份（provinces 表中不存在）: {missing_provinces}")

        # 提示待补充项
        todo_provinces = [
            "西藏", "青海",
            "四川（特殊类型）", "陕西（特殊类型）",
            "山西（本科线）", "江西（本科线）",
            "贵州（本科线）", "云南（本科线）",
            "甘肃（本科线）", "宁夏（本科线）",
        ]
        print(f"\n⏳ 以下数据截至 2026-06-25 尚未公布，待各省教育考试院官网更新后补充：")
        for p in todo_provinces:
            print(f"  - {p}")

    except Exception:
        db.rollback()
        raise
    finally:
        db.close()


if __name__ == "__main__":
    run()
